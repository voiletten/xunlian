I = int(input("利润"))

bonus = 0

if I <= 10:
    bonus = I * 0.1
elif I <= 20:
    bonus = 10 * 0.1 + (I - 10) * 0.075
elif I <= 40:
    bonus = 10 * 0.1 + 10 * 0.075 + (I - 20) * 0.05
elif I <= 60:
    bonus = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (I - 40) * 0.03
elif I <= 100:
    bonus = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + (I - 60) * 0.015
else:
    bonus = (
        10 * 0.1 +
        10 * 0.075 +
        20 * 0.05 +
        20 * 0.03 +
        40 * 0.015 +
        (I - 100) * 0.01
    )

print(f"奖金为：{bonus:.2f} 万")